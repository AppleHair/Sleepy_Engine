DROP TABLE IF EXISTS folder;
CREATE TABLE folder(  rowid INTEGER PRIMARY KEY AUTOINCREMENT, name TEXT, color TEXT, container INTEGER );
DROP TABLE IF EXISTS asset;
CREATE TABLE asset(  rowid INTEGER PRIMARY KEY AUTOINCREMENT, name TEXT, type INTEGER, container INTEGER, config INTEGER, data INTEGER );
DROP TABLE IF EXISTS element;
CREATE TABLE element( rowid INTEGER PRIMARY KEY AUTOINCREMENT, name TEXT, type INTEGER, container INTEGER, config INTEGER, script INTEGER );
DROP TABLE IF EXISTS blobs;
CREATE TABLE blobs( rowid INTEGER PRIMARY KEY AUTOINCREMENT, data BLOB );
